package com.internalaudit.shared;

import java.io.Serializable;

public class SkillUpdateData implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	private int skillId;
	
	private int hoursToSubt;
	private int hoursToAdd;// For Updating a job hours, First we will add the old hours of that job and then subtract the new hours
	

	public int getSkillId() {
		return skillId;
	}

	public void setSkillId(int skillId) {
		this.skillId = skillId;
	}

	public int getHoursToSubt() {
		return hoursToSubt;
	}

	public void setHoursToSubt(int hoursToSubt) {
		this.hoursToSubt = hoursToSubt;
	}

	public int getHoursToAdd() {
		return hoursToAdd;
	}

	public void setHoursToAdd(int hoursToAdd) {
		this.hoursToAdd = hoursToAdd;
	}

}
